INSERT
	INTO nfl_team_qb (conference_id, division_id, team_id, conference_name, division_name, team_city, team_mascot, qb_first_name, qb_last_name )
	SELECT t.conference_id, t.division_id, t.team_id, t.conference_name, t.division_name, t.team_city, t.team_mascot, qb.qb_first_name, qb.qb_last_name
			FROM nfl_team t
            JOIN nfl_qb qb
            ON qb.conference_id = t.conference_id
            AND qb.division_id = t.division_id
            AND qb.team_id = t.team_id;
            
INSERT
	INTO nfl_team_wr (conference_id, division_id, team_id, conference_name, division_name, team_city, team_mascot, wr_first_name, wr_last_name )
